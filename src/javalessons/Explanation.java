package javalessons;

public class Explanation {

//    Сериализация - это преобразование экземпляра класса в форму, пригодную для его сохранения (например в файл, в БД
//    или для передачи по сети). Сериализованные объекты можно затем восстановить (десериализовать).
//
//    Свойства класса, помеченные модификатором transient, не сериализуются.
//
//    Обычно в таких полях хранится промежуточное состояние объекта, которое, к примеру, проще вычислить, чем
//    сериализовать, а затем десериализовать. Другой пример такого поля — ссылка на экземпляр объекта, который не
//    требует сериализации или не может быть сериализован.
}
